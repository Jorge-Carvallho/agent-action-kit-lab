ROOT = Path(__file__).resolve().parent


def main() -> None:
    if len(_ROWS) < 100:
        raise SystemExit(f"Esperava ≥100 exames, obtive {len(_ROWS)}")
    exams = []
    for i, (name, short, syn) in enumerate(_ROWS, start=1):
        code = f"LAB-F{i:04d}"
        exams.append(
            {
                "code": code,
                "name": name,
                "short_description": short,
                "synonyms": syn,
            }
        )
    payload = {
        "schema_version": 1,
        "catalog_id": "mock-lab-exams-v1",
        "description": "Base fictícia para RAG no desafio laboratorial (≥100 exames).",
        "record_count": len(exams),
        "exams": exams,
    }
    out = ROOT / "exams_mock.json"
    out.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    print(f"Escrito {out} com {len(exams)} registos.")


if __name__ == "__main__":
    main()
